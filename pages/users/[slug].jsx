import React from 'react';//se puede poner y no, ya que estamos trabajando con react
import Typography from '@mui/material/Typography';
import { Container, Grid, Paper, Chip, TextField, FormControl, InputLabel, Select, MenuItem, Autocomplete, NativeSelect, Breadcrumbs } from '@mui/material';
import { useEffect, useState } from 'react';
import Head from 'next/head';
import apiClient from 'apiClient'
import {useRouter} from 'next/router';

export default function Products(){
    const router = useRouter();
    const [users,setUsers] = useState([])
    //console.log(router)

    useEffect(() => {
        if(router.query.slug){
        apiClient.get(`/api/users/${router.query.slug}`)
        .then((response) => {
            //console.log(response.data)
            //alert('si conecto');
            setUsers(response.data)
        })
        .catch((error) => {
            alert("Error al visualizar usuario, intente m√°s tarde")
        });
        }
    }, [router]);

    console.log(users)
    return (
        <>
        <Head>
            <title>Usuario</title>
            <meta name="description" content="Generated by create next app" />
            <meta name="viewport" content="width=device-width, initial-scale=1" />
            <link rel="icon" href="/favicon.ico" />
        </Head>


        <Paper>
        <h1>Informacion del usuario</h1>
        
        <Typography>Nombre: {users?.name}</Typography>
        <Typography>Apellidos: {users?.lastName}</Typography>
        
    <Container>
    </Container>
    </Paper>

</>
    )
}